// Language colors matching server/github.ts LANGUAGE_COLORS map
const LANGUAGE_COLORS: Record<string, string> = {
    TypeScript: "#3178c6",
    JavaScript: "#f1e05a",
    Python: "#3572A5",
    Java: "#b07219",
    Go: "#00ADD8",
    Rust: "#dea584",
    Ruby: "#701516",
    PHP: "#4F5D95",
    "C++": "#f34b7d",
    C: "#555555",
    "C#": "#178600",
    Swift: "#F05138",
    Kotlin: "#A97BFF",
    Dart: "#00B4AB",
    Shell: "#89e051",
    HTML: "#e34c26",
    CSS: "#563d7c",
    Vue: "#41b883",
    Svelte: "#ff3e00",
};

const FALLBACK_COLOR = "#8b8b8b";

export function getLanguageColor(language: string | null): string {
    if (!language) return FALLBACK_COLOR;
    return LANGUAGE_COLORS[language] ?? FALLBACK_COLOR;
}